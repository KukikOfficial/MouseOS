#include "include/font.h"
#include "include/vesa.h"

// Шрифт 8x8. Каждый символ занимает 8 байт.
// Чтобы найти символ 'A' (ASCII 65), нужно перейти на 65 * 8 байт вглубь.
unsigned char font_data[128][8] = {
    [32] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // Пробел
    [48] = {0x38, 0x44, 0x4C, 0x54, 0x64, 0x44, 0x38, 0x00}, // 0
    [49] = {0x10, 0x30, 0x10, 0x10, 0x10, 0x10, 0x38, 0x00}, // 1
    [65] = {0x10, 0x28, 0x44, 0x44, 0x7C, 0x44, 0x44, 0x00}, // A
    [66] = {0x78, 0x44, 0x44, 0x78, 0x44, 0x44, 0x78, 0x00}, // B
    [67] = {0x38, 0x44, 0x40, 0x40, 0x40, 0x44, 0x38, 0x00}, // C
    [69] = {0x7C, 0x40, 0x40, 0x78, 0x40, 0x40, 0x7C, 0x00}, // E
    [77] = {0x44, 0x6C, 0x54, 0x54, 0x44, 0x44, 0x44, 0x00}, // M
    [79] = {0x38, 0x44, 0x44, 0x44, 0x44, 0x44, 0x38, 0x00}, // O
    [83] = {0x38, 0x44, 0x30, 0x08, 0x04, 0x44, 0x38, 0x00}, // S
    [85] = {0x44, 0x44, 0x44, 0x44, 0x44, 0x44, 0x38, 0x00}, // U
};

void draw_char(int x, int y, char c, uint32_t color) {
    // В Mouse Core используем только стандартные ASCII
    int index = (unsigned char)c;
    if (index >= 128) return;

    for (int i = 0; i < 8; i++) {
        unsigned char line = font_data[index][i];
        for (int j = 0; j < 8; j++) {
            if (line & (0x80 >> j)) {
                put_pixel(x + j, y + i, color);
            }
        }
    }
}

void draw_string(int x, int y, const char* str, uint32_t color) {
    int cur_x = x;
    int cur_y = y;
    for (int i = 0; str[i] != '\0'; i++) {
        if (str[i] == '\n') {
            cur_y += 12; // Переход на новую строку
            cur_x = x;
        } else {
            draw_char(cur_x, cur_y, str[i], color);
            cur_x += 8;
        }
    }
}
