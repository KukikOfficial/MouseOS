void draw_char(int x, int y, char c, uint32_t color) {
    // ИСПРАВЛЕНО: Если пришла маленькая буква, переводим её в большую (c -= 32)
    if (c >= 'a' && c <= 'z') {
        c -= 32;
    }

    for (int i = 0; i < 8; i++) {
        unsigned char bits = get_font_line(c, i);
        for (int j = 0; j < 8; j++) {
            if (bits & (0x80 >> j)) {
                put_pixel(x + j, y + i, color);
            }
        }
    }
}